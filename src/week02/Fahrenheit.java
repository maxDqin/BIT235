class Fahrenheit {

/* easiest programming problem for lesson2 */

/* This program converts 32 degrees C to degrees F
   A simple example of a java program */

   public static void main (String [] args) {
	   double C;
	   double F;
	   C = 32.0;
	   F = C*9/5 + 32;
       System.out.println(C + " degrees C= " + F + " degrees in F");
   }
}